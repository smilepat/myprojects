#!/usr/bin/env node
/**
 * Auto-generate docs/INDEX.md from filesystem.
 *
 * Scans docs/{01-plan,02-design,03-analysis,04-report}/*.md, reads each
 * file's first H1 heading + the "Status:" line if present, and emits a
 * grouped table-of-contents.
 *
 * Run manually: node scripts/build-docs-index.mjs
 * Run check:    node scripts/build-docs-index.mjs --check (exit 1 if stale)
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, basename } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DOCS = join(ROOT, "docs");

const GROUPS = [
  { dir: "01-plan", title: "01 · Plan (PRD + 백로그)" },
  { dir: "02-design", title: "02 · Design (Phase 별 설계)" },
  { dir: "03-analysis", title: "03 · Analysis (검증 / dogfooding / runbook)" },
  { dir: "04-report", title: "04 · Report (W별 진행 + 종합 회고)" },
];

function extractMeta(filePath) {
  const text = readFileSync(filePath, "utf-8");
  const titleMatch = text.match(/^#\s+(.+)$/m);
  const statusMatch = text.match(/^>\s*Status:\s*(.+)$/m);
  const dateMatch = text.match(/^>\s*(?:작성|실시|실행|Last updated):\s*([\d-]+)/m);
  return {
    title: titleMatch ? titleMatch[1].trim() : basename(filePath, ".md"),
    status: statusMatch ? statusMatch[1].trim().replace(/^\*\*|\*\*$/g, "").trim() : null,
    date: dateMatch ? dateMatch[1] : null,
  };
}

function generateIndex() {
  const sections = [];
  sections.push("# OELP Docs Index");
  sections.push("");
  sections.push(`> Auto-generated by \`scripts/build-docs-index.mjs\`. Do not edit by hand — run the script.`);
  sections.push(`> Last generated: ${new Date().toISOString().slice(0, 10)}`);
  sections.push("");

  let totalCount = 0;
  const groupCounts = [];
  for (const group of GROUPS) {
    const dir = join(DOCS, group.dir);
    if (!existsSync(dir)) continue;
    const files = readdirSync(dir)
      .filter((f) => f.endsWith(".md"))
      .sort();
    groupCounts.push(`${group.dir}: ${files.length}`);
    totalCount += files.length;
  }

  sections.push(`**총 ${totalCount} 문서** (${groupCounts.join(" · ")})`);
  sections.push("");

  for (const group of GROUPS) {
    const dir = join(DOCS, group.dir);
    if (!existsSync(dir)) continue;
    const files = readdirSync(dir)
      .filter((f) => f.endsWith(".md"))
      .sort();
    if (files.length === 0) continue;

    sections.push(`## ${group.title}`);
    sections.push("");
    sections.push("| 파일 | 제목 | 상태 | 날짜 |");
    sections.push("|---|---|---|---|");
    for (const file of files) {
      const meta = extractMeta(join(dir, file));
      const relPath = `${group.dir}/${file}`;
      const titleCell = meta.title.length > 60 ? meta.title.slice(0, 60) + "…" : meta.title;
      const statusCell = meta.status
        ? meta.status.length > 50
          ? meta.status.slice(0, 50) + "…"
          : meta.status
        : "—";
      sections.push(`| [\`${file}\`](./${relPath}) | ${titleCell} | ${statusCell} | ${meta.date ?? "—"} |`);
    }
    sections.push("");
  }

  sections.push("---");
  sections.push("");
  sections.push("### 갱신 정책");
  sections.push("");
  sections.push("- 새 문서 추가 / 제목 변경 / Status 변경 시 본 스크립트 재실행");
  sections.push("- `node scripts/build-docs-index.mjs` (수동)");
  sections.push("- `node scripts/build-docs-index.mjs --check` (CI 검증, exit 1 if stale)");
  sections.push("");
  sections.push("### 관련 자료");
  sections.push("");
  sections.push("- [smilepat/oelp](https://github.com/smilepat/oelp) — 본 docs가 다루는 구현체 (282 tests · 7 routes · 19 lib modules)");
  sections.push("- [CLAUDE.md](../CLAUDE.md) — Claude Code 작업 가이드");
  sections.push("- README (myprojects 루트) — LogicFlow 생태계 47+ 레포 소개");

  return sections.join("\n") + "\n";
}

const INDEX_PATH = join(DOCS, "INDEX.md");
const newContent = generateIndex();

const checkOnly = process.argv.includes("--check");

if (checkOnly) {
  if (!existsSync(INDEX_PATH)) {
    console.error("docs/INDEX.md missing. Run: node scripts/build-docs-index.mjs");
    process.exit(1);
  }
  const existing = readFileSync(INDEX_PATH, "utf-8");
  // Compare ignoring the "Last generated" line (changes every run)
  const stripDate = (s) => s.replace(/^> Last generated:.*$/m, "");
  if (stripDate(existing) !== stripDate(newContent)) {
    console.error("docs/INDEX.md out of date. Run: node scripts/build-docs-index.mjs");
    process.exit(1);
  }
  console.log("docs/INDEX.md in sync.");
  process.exit(0);
}

writeFileSync(INDEX_PATH, newContent);
console.log(`Wrote ${INDEX_PATH} (${newContent.length} bytes)`);
